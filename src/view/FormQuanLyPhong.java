    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkInPut()){
            Connection con=null;
            CallableStatement callSt=null;
        try {
            con=ConnectionDB.openConnection();
            callSt=con.prepareCall("{call insertP(?,?,?,?,?)}");
            callSt.setString(1, txtMaP.getText());
            callSt.setString(2, txtTen.getText());
            callSt.setString(3, txtGia.getText());
            callSt.setString(4, txtTrangThai.getText());
            callSt.setString(5, txtMaLP.getText());
            callSt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            showData();
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            ConnectionDB.closeConnection(con, callSt);
        }
        }
        else{
            JOptionPane.showMessageDialog(this, "Them that bai");
            showData();
        }
        
    }//GEN-LAST:event_btnThemActionPerformed

