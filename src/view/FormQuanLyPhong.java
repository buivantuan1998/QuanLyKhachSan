    public boolean checkInPut(){
        if (txtGia.getText().equals("") || txtMaLP.getText().equals("") || txtMaP.getText().equals("") || txtTen.getText().equals("") || txtTrangThai.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui long dien day du thong tin");
            return false;
        }
        else{
            return true;
        }
    }
    
    public List<Phong> getDataCV(){
        Connection con=null;
        CallableStatement callSt=null;
        List<Phong> listphong=new ArrayList<>();
        try {
            con=ConnectionDB.openConnection();
            callSt=con.prepareCall("{call getAllP()}");
            ResultSet rs=callSt.executeQuery();
            while (rs.next()) {                
                Phong p= new Phong();
                p.setMaP(rs.getString("MaP"));
                p.setTenP(rs.getString("TenP"));
                p.setGiaThue(rs.getFloat("GiaThue"));
                p.setTrangThai(rs.getInt("TrangThai"));
                p.setMaLP(rs.getString("MaLP"));
                listphong.add(p);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            ConnectionDB.closeConnection(con, callSt);
        }
        return listphong;
    }
    DefaultTableModel dtbm;
    public void showData(){
        List<Phong> list=getDataCV();
         dtbm=(DefaultTableModel) TableP.getModel();
        
        dtbm.setRowCount(0);
        
        Object [] row=new Object[5];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getMaP();
            row[1]=list.get(i).getTenP();
            row[2]=list.get(i).getGiaThue();
            row[3]=list.get(i).getTrangThai();
            row[4]=list.get(i).getMaLP();
            dtbm.addRow(row);
        }
    }
    public void filter(String query){
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(dtbm);
        TableP.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }

