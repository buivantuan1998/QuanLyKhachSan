    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkInput()){
            Connection con=null;
            CallableStatement callSt=null;
        try {
            con=ConnectionDB.openConnection();
            callSt=con.prepareCall("{call insertKH(?,?,?,?,?,?)}");
            callSt.setString(1, txtMaKH.getText());
            callSt.setString(2, txtTenKH.getText());
            callSt.setString(3, (String)txtGT.getSelectedItem());
            callSt.setString(4, txtSoCMT.getText());
            callSt.setString(5, txtSDT.getText());
            callSt.setString(6, txtDC.getText());
            callSt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            showData();
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            ConnectionDB.closeConnection(con, callSt);
        }
        }
        else{
            JOptionPane.showMessageDialog(this, "Them that bai");
            showData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

