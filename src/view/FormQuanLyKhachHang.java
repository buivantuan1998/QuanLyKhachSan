    public List<KhachHang> getDataCV(){
        Connection con=null;
        CallableStatement callSt=null;
        List<KhachHang> listkhachhang=new ArrayList<>();
        try {
            con=ConnectionDB.openConnection();
            callSt=con.prepareCall("{call getAllKH()}");
            ResultSet rs=callSt.executeQuery();
            while (rs.next()) {                
                KhachHang p= new KhachHang();
                p.setMaKH(rs.getString("MaKH"));
                p.setTenKH(rs.getString("TenKH"));
                p.setGioiTinh(rs.getString("GioiTinh"));
                p.setSoCMT(rs.getString("SoCMT"));
                p.setSdt(rs.getString("SDT"));
                p.setDiaChi(rs.getString("DiaChi"));
                listkhachhang.add(p);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            ConnectionDB.closeConnection(con, callSt);
        }
        return listkhachhang;
    }
    DefaultTableModel dtbm;
    public void showData(){
        List<KhachHang> list=getDataCV();
         dtbm=(DefaultTableModel) TableKH.getModel();
        
        dtbm.setRowCount(0);
        
        Object [] row=new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getMaKH();
            row[1]=list.get(i).getTenKH();
            row[2]=list.get(i).getGioiTinh();
            row[3]=list.get(i).getSoCMT();
            row[4]=list.get(i).getSdt();
            row[5]=list.get(i).getDiaChi();
            dtbm.addRow(row);
        }
    }

    public void filter(String query){
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(dtbm);
        TableKH.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }
